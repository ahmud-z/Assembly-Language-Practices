
.STACK 100H
.DATA
ARR DW 1,2,3,4,5,6,8

LEN DW ($-ARR)/2              
SEARCH_VALUE EQU 8

MSG1 DB "VALUE FOUND AT "
RES DB "  POSITION",13,10,"$"
MSG2 DB 'KEY NOT FOUND $'

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    MOV BX,00
    MOV DX,LEN
    MOV CX,SEARCH_VALUE

AGAIN:
    CMP BX,DX
    JA FAIL
    MOV AX,BX
    ADD AX,DX

    SHR AX,1
    MOV SI,AX
    ADD SI,SI
    CMP CX,ARR[SI]
    
    JAE GREATER_ELEMENT
    DEC AX
    MOV DX,AX
    JMP AGAIN 
      
GREATER_ELEMENT:
    JE SUCCESS
    INC AX
    MOV BX,AX
    JMP AGAIN
    
SUCCESS:
    ADD AL,01
    ADD AL,'0'
    MOV RES,AL
    LEA DX,MSG1
    JMP DISPLAY_MSG   
FAIL: 
    LEA DX,MSG2
    
DISPLAY_MSG:
    MOV AH,09H
    INT 21H
    MOV AH,4CH
END MAIN
RET




