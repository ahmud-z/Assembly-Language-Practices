S_DEPT DB 'DEPT:CSE $'
S_UNI DB 'GREEN UNIVERSITY OF BANGLADESH $'
S_LOCATION DB 'PURBACAL, AMERICAN CITY $'
 
.CODE
MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    

    LEA DX,S_NAME
    CALL PRINT
    
    LEA DX,S_ID
    CALL PRINT
    
    LEA DX,S_DEPT
    CALL PRINT 
    
    LEA DX,S_UNI
    CALL PRINT 
    
    LEA DX,S_LOCATION
    CALL PRINT 
    
    
    MOV AH,4CH
    MOV AL,00H
    INT 21H
    
    MAIN ENDP


PRINT PROC
    MOV AH,09H
    INT 21H
    
    
    MOV DL,10
    MOV AH,02H
    INT 21H
